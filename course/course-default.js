/**
 * 获取教授评语（随机取1条）
 * @param {string} subjectKey  科目键名
 * @param {string} rating      评级 O/E/A/P/D/T
 * @returns {string}
 */
export function getProfessorComment(subjectKey, rating) {
  const bank = professorComments[subjectKey];
  if (!bank) return "";
  const list = bank[rating];
  if (!list || list.length === 0) return "";
  return list[Math.floor(Math.random() * list.length)];
}

